/** 
* Copute path as vector of vertices from s to t given the pred results 
* from an allP[airShortest execution. Note that s and t must be valid.
* integer vertex identifiers. If no p-ath is found between s and t, then an
* empty path is returned.
*/ 
void constructShortestPath(int s, int t, /* in*/
							vector< vector<int> > const &pred, /* in */
							list<int> &path) {	/*out */
	path.clear();
	if (t < 0 || t >= (int) pred.size() || s < 0 || s >= (int) pred.size()) {
		return;
		}
		
		// construct path untill we hit source 's' or -1 if there is no path.
		path.push_front(t);
		while (t != s) {
			t = pred[s][t];
			if (t == -1) { path.clear(); return;}
			
			path.push_front(t);
			}
		}
		